package CV_Bank.model;

public class FooterLinksData {
    private String linkedinLink;
    private String girHub;
    private String portfolio;

    public String getLinkedinLink() {
        return linkedinLink;
    }

    public FooterLinksData setLinkedinLink(String linkedinLink) {
        this.linkedinLink = linkedinLink;
        return this;
    }

    public String getGirHub() {
        return girHub;
    }

    public FooterLinksData setGirHub(String girHub) {
        this.girHub = girHub;
        return this;
    }

    public String getPortfolio() {
        return portfolio;
    }

    public FooterLinksData setPortfolio(String portfolio) {
        this.portfolio = portfolio;
        return this;
    }
}
